package week1.day2;

public class Factorial {

	public static void main(String[] args) {
		int num=5;
		int fact=num;     
		for(int i=num;i>1;i--)
		{
			                          
			fact=fact*(i-1); 
			}
		System.out.println("the factorial of "+" "+num+" "+"is"+" "+fact);	
	
	
	
	}}

